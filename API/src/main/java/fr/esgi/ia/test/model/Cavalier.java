public class Cavalier extends Piece implements PieceRule {

	// =========================================================================
	// ATTRIBUTES
	// =========================================================================

	// =========================================================================
	// CONSTRUCTORS
	// =========================================================================

	public Cavalier() {
		super();
	}

	// =========================================================================
	// METHODS
	// =========================================================================

	// =========================================================================
	// OVERRIDES
	// =========================================================================

	@Override
	public boolean CanGoTo(Position _pos) {
		return (_pos.dx(getPosition()) == 1 && _pos.dy(getPosition()) == 2)
				|| (_pos.dx(getPosition()) == 2 && _pos.dy(getPosition()) == 1);
	}

	// =========================================================================
	// GETTERS & SETTERS
	// =========================================================================

}
