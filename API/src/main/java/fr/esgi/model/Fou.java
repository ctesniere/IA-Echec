public class Fou extends Piece {

	// =========================================================================
	// ATTRIBUTES
	// =========================================================================

	// =========================================================================
	// CONSTRUCTORS
	// =========================================================================

	public Fou() {
		super();
	}

	// =========================================================================
	// METHODS
	// =========================================================================

	public boolean CanGoTo(Position _pos) {
		return !_pos.equals(getPosition()) && _pos.isInDiagonale(getPosition());
	}

	// =========================================================================
	// OVERRIDES
	// =========================================================================

	@Override
	public String toString() {
		return this.getClass().getName();
	}

	// =========================================================================
	// GETTERS & SETTERS
	// =========================================================================

}
